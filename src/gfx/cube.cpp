#include "gfx/cube.h"

#include <array>
#include <cstdint>

namespace gfx {

void build_unit_cube(Mesh& out) {
    // 6 faces * 4 verts = 24 verts. Each face has flat normal, full 0..1 UV.
    const std::array<VertexPNT, 24> verts = {{
        // +X
        {{ 0.5f,-0.5f,-0.5f}, { 1, 0, 0}, {0,0}},
        {{ 0.5f, 0.5f,-0.5f}, { 1, 0, 0}, {1,0}},
        {{ 0.5f, 0.5f, 0.5f}, { 1, 0, 0}, {1,1}},
        {{ 0.5f,-0.5f, 0.5f}, { 1, 0, 0}, {0,1}},
        // -X
        {{-0.5f,-0.5f, 0.5f}, {-1, 0, 0}, {0,0}},
        {{-0.5f, 0.5f, 0.5f}, {-1, 0, 0}, {1,0}},
        {{-0.5f, 0.5f,-0.5f}, {-1, 0, 0}, {1,1}},
        {{-0.5f,-0.5f,-0.5f}, {-1, 0, 0}, {0,1}},
        // +Y (top)
        {{-0.5f, 0.5f,-0.5f}, { 0, 1, 0}, {0,0}},
        {{-0.5f, 0.5f, 0.5f}, { 0, 1, 0}, {1,0}},
        {{ 0.5f, 0.5f, 0.5f}, { 0, 1, 0}, {1,1}},
        {{ 0.5f, 0.5f,-0.5f}, { 0, 1, 0}, {0,1}},
        // -Y (bottom)
        {{-0.5f,-0.5f, 0.5f}, { 0,-1, 0}, {0,0}},
        {{-0.5f,-0.5f,-0.5f}, { 0,-1, 0}, {1,0}},
        {{ 0.5f,-0.5f,-0.5f}, { 0,-1, 0}, {1,1}},
        {{ 0.5f,-0.5f, 0.5f}, { 0,-1, 0}, {0,1}},
        // +Z
        {{-0.5f,-0.5f, 0.5f}, { 0, 0, 1}, {0,0}},
        {{ 0.5f,-0.5f, 0.5f}, { 0, 0, 1}, {1,0}},
        {{ 0.5f, 0.5f, 0.5f}, { 0, 0, 1}, {1,1}},
        {{-0.5f, 0.5f, 0.5f}, { 0, 0, 1}, {0,1}},
        // -Z
        {{ 0.5f,-0.5f,-0.5f}, { 0, 0,-1}, {0,0}},
        {{-0.5f,-0.5f,-0.5f}, { 0, 0,-1}, {1,0}},
        {{-0.5f, 0.5f,-0.5f}, { 0, 0,-1}, {1,1}},
        {{ 0.5f, 0.5f,-0.5f}, { 0, 0,-1}, {0,1}},
    }};

    std::array<std::uint32_t, 36> idx{};
    for (std::uint32_t face = 0; face < 6; ++face) {
        std::uint32_t base = face * 4;
        std::uint32_t* i = &idx[face * 6];
        i[0] = base + 0; i[1] = base + 1; i[2] = base + 2;
        i[3] = base + 0; i[4] = base + 2; i[5] = base + 3;
    }

    out.upload(verts, idx);
}

}  // namespace gfx
